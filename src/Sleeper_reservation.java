public void Select(JToggleButton b)
    {
        System.out.println("called \n");
        DefaultTableModel model=(DefaultTableModel)jTable1.getModel();
        if(b.isSelected())
        {
            if(seat_selected<4)
            {
                String seat = b.getText();
                b.setBackground(Color.GREEN);
                seat_selected++;
                model.addRow(new Object[] {null,null,null,seat});
            }
            else
            {
                b.setSelected(false);
                b.setBackground(Color.WHITE);
                JOptionPane.showMessageDialog(this,"Maximum number of Seats Selection : 4");
               
            }
        }
        else
        {
            b.setBackground(Color.WHITE);
            seat_selected--;
            String seat = b.getText();
            int rows = model.getRowCount();
            for(int i =0;i<rows;i++)
            {
                if(model.getValueAt(i,3).equals(seat))
                {
                    model.removeRow(i);
                    break;
                }
            }
        }
        
    }
