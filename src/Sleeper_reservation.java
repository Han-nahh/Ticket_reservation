    private void jLabel6MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel6MouseClicked
        // TODO add your handling code here:
        if(jCheckBox2.isSelected())
        {
            boolean flag = true;
            String name,G;
            String age;
            int s;
            DefaultTableModel model=(DefaultTableModel)jTable1.getModel();
            int rows  = model.getRowCount();
            int i=0;
            for( i =0;i<rows;i++)
            {
                name = model.getValueAt(i, 0).toString();
                age= model.getValueAt(i, 1).toString();
                G= model.getValueAt(i, 2).toString();
                if(!(Passenger_insertion.validate_age(age) && Passenger_insertion.validate_gender(G)&&Passenger_insertion.validate_username(name)))
                {
                    flag=false;
                    JOptionPane.showMessageDialog(this,"Invalid Entries found");
                    break; 
                }
            }
            if(i==rows && flag)
            {
                String date_s = jLabel12.getText(); 
                SimpleDateFormat dt = new SimpleDateFormat("dd/MM/yyyy"); 
                Date date = null; 
                try 
                {
                  date = dt.parse(date_s);
                } catch (Exception e) {
                    System.out.println(e);
                }
                SimpleDateFormat dt1 = new SimpleDateFormat("yyyy-MM-dd");
                Passenger_insertion.insertintoBooking(dt1.format(date),rows,Selection.numbers[Bus_reservations.select_bus]);
                for( i =0;i<rows;i++)
                {
                    name = model.getValueAt(i, 0).toString();
                    age= model.getValueAt(i, 1).toString();
                    G=model.getValueAt(i, 2).toString();
                    s=Integer.parseInt(model.getValueAt(i, 3).toString());
                    Passenger_insertion.insertintoDB(name,Integer.parseInt(age),G.toUpperCase(),s,Selection.fare[Bus_reservations.select_bus]);
                }
                
                Passenger_insertion.ticket_details();
                this.setVisible(false);
            }
            
        }
        else{
            JOptionPane.showMessageDialog(this,"Please confirm your Details");
        }
    }//GEN-LAST:event_jLabel6MouseClicked

